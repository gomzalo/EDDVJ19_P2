    public void llenarCBs(){
        if(!estructuras.Estructuras.th_estudiantes.esVacia()){
            for (Estudiante e : estructuras.Estructuras.th_estudiantes.tabla) {
                if(e != null){
                    carnet_cb.addItem(String.valueOf(e.getCarnet()));
                }
            }
        }
        if(!estructuras.Estructuras.b_horarios.esVacio()){
            ColaB cola_auxiliar = estructuras.Estructuras.b_horarios.listar();
            int tamano_cola = estructuras.Estructuras.b_horarios.listar().getTamano();
            for(int i = 0; i < tamano_cola; i++){
                codigo_horario_cb.addItem(String.valueOf(cola_auxiliar.desencolar()
                .getHorario().getCodigo()));
            }
        }
    }

