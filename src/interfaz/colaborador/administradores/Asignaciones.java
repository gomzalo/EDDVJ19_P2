    private void agregar_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregar_btActionPerformed
        // TODO add your handling code here:
        if(!zona_txt.getText().isEmpty() || !final_txt.getText().isEmpty()){
            
            int carnet = Integer.parseInt(carnet_cb.getSelectedItem().toString());
            int codigo_horario = Integer.parseInt(codigo_horario_cb.getSelectedItem().toString());
            int zona = Integer.parseInt(zona_txt.getText());
            int nota_final = Integer.parseInt(final_txt.getText());
            
            if(estructuras.Estructuras.b_horarios.buscarPorParametro(codigo_horario) != null){
                try {
                    if(estructuras.Estructuras.b_horarios.buscarPorParametro(codigo_horario).getAsignaciones() != null 
                    || !estructuras.Estructuras.b_horarios.buscarPorParametro(codigo_horario).getAsignaciones().esVacia()){
                        ListaS lista_asignaciones_temporales = new ListaS();

                        lista_asignaciones_temporales.insertar(
                        new NodoS(new pojos.Asignacion(carnet, codigo_horario, zona, nota_final), 
                        estructuras.Estructuras.th_estudiantes.buscar(carnet)));

                        estructuras.Estructuras.b_horarios.
                        buscarPorParametro(codigo_horario).
                        setAsignaciones(lista_asignaciones_temporales);
                    }else{
                        estructuras.Estructuras.b_horarios.
                        buscarPorParametro(codigo_horario).
                        getAsignaciones().insertar(new NodoS(
                        new pojos.Asignacion(carnet, codigo_horario, zona, nota_final),
                        estructuras.Estructuras.th_estudiantes.buscar(carnet)));
                    }
                    JOptionPane.showMessageDialog(null, "Se ingreso correctamente la asignacion del carnet: "
                    + carnet + ", al horario: " + codigo_horario + ".", 
                    "Muy bien", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    System.out.println("No tiene asignaciones");
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "No se ingreso la asignacion del carnet: "
                + carnet + ", porque ya existe este en el horario: " + codigo_horario + ".", 
                "Atencion", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "¡Hay campos vacios!",
            "Atencion", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_agregar_btActionPerformed

